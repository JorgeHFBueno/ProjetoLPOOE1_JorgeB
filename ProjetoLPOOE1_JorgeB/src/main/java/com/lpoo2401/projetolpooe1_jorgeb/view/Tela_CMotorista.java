    private void loadComboBoxData() {
        EntityManager em = emf.createEntityManager();

        // Load Caminhao entities
        List<Caminhao> caminhoes = em.createQuery("SELECT c FROM Caminhao c", Caminhao.class).getResultList();
        DefaultComboBoxModel<Caminhao> caminhaoModel = new DefaultComboBoxModel<>(caminhoes.toArray(new Caminhao[0]));
        jComboBox_Caminhao.setModel(caminhaoModel);

        em.close();
    }
    // Metodo para preencher os campos na edicao
