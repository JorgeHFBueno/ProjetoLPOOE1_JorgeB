    private void jButton_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SalvarActionPerformed
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();

        try {
            if (motorista == null) {
                // Adding new Motorista
                motorista = new Motoristas();
                motorista.setCPF(Integer.parseInt(jTextField_CPF.getText()));
            }
            // Setting/Updating values
            motorista.setNome(jTextField_Nome.getText());
            motorista.setNumero_celular(jTextField_Cel.getText());
            motorista.setCaminhao((Caminhao) jComboBox_Caminhao.getSelectedItem());

            if (motorista.getCPF() != null) {
                em.merge(motorista);  // Merges the new or updated Motorista into the database
            } else {
                em.persist(motorista);
            }

            em.getTransaction().commit();
            parentFrame.attList();  // Refresh the list in the parent frame
            this.dispose();  // Close the form
        } catch (Exception e) {
            em.getTransaction().rollback();
            JOptionPane.showMessageDialog(this, "Erro na tentativa de salvar o Motorista: " + e.getMessage());
        } finally {
            em.close();
        }
    }//GEN-LAST:event_jButton_SalvarActionPerformed

